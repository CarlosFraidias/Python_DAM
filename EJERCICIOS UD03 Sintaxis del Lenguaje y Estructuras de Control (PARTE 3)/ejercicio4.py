"""Dados dos números, mostrar la suma, resta, división y multiplicación de ambos."""

num1 = int(input("Introduce el primer número: "))
num2 = int(input("Introduce el segudo número: "))

suma = num1 + num2
resta = num1 - num2
prod = num1 * num2
div = num1 / num2

print(f"Suma: {suma}\nResta: {resta}\nMultiplicación: {prod}\nDivisión: {div}")